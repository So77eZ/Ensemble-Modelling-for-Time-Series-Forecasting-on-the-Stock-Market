# -----------------------------------------------------------------------------
# LSTM
# -----------------------------------------------------------------------------

# Длина входной последовательности (количество предыдущих торговых дней)
LSTM_LOOK_BACK: int = 30

# Максимальное число эпох обучения (EarlyStopping остановит раньше при необходимости)
LSTM_EPOCHS: int = 10

# Число эпох без улучшения val_loss до остановки (EarlyStopping patience)
LSTM_PATIENCE: int = 3

# Размер батча при обучении
LSTM_BATCH_SIZE: int = 32

# Начальная скорость обучения оптимизатора Adam
LSTM_LEARNING_RATE: float = 0.001

# Доля нейронов, отключаемых на каждом шаге (регуляризация Dropout)
LSTM_DROPOUT_RATE: float = 0.2

# Количество нейронов в первом LSTM-слое (второй = units // 2)
LSTM_UNITS: int = 64

# -----------------------------------------------------------------------------
# XGBoost
# -----------------------------------------------------------------------------

# Количество деревьев в ансамбле
XGBOOST_N_ESTIMATORS: int = 100

# Максимальная глубина дерева
XGBOOST_MAX_DEPTH: int = 6

# Скорость обучения (shrinkage)
XGBOOST_LEARNING_RATE: float = 0.1

# Доля строк обучающей выборки для построения каждого дерева
XGBOOST_SUBSAMPLE: float = 0.8

# Доля признаков для построения каждого дерева
XGBOOST_COLSAMPLE_BYTREE: float = 0.8

# Зерно генератора случайных чисел (воспроизводимость)
XGBOOST_RANDOM_STATE: int = 42

# Уровень логирования XGBoost (0 = тихий)
XGBOOST_VERBOSITY: int = 0

# -----------------------------------------------------------------------------
# Output
# -----------------------------------------------------------------------------

# Корневая директория для сохранения результатов, логов и графиков
OUTPUT_ROOT: str = 'outputs'
